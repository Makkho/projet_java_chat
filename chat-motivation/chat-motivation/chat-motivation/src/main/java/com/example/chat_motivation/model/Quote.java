package com.example.chat_motivation.model;

public class Quote {
    private Long id;
    private String text;

    public Quote() {
    	
    	
    }

    public Long getId() {
        return id;
    }
    public String getText() {
        return text;
    }
    public void setId(Long id) {
        this.id = id;
    }
    public void setText(String text) {
        this.text = text;
    }
    
    
}
